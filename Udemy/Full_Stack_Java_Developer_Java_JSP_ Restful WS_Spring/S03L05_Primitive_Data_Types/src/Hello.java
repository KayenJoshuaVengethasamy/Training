
public class Hello {

	public static void main(String[] args) {
// int is integer that is used for a whole number
// this is the maximum the int data type can store
		int intMax = 2147483647;
// this is the minimum the int data type can store
		int intMin = -2147483648;
// short is similar to integer, but it stores smaller values
// this is the maximum the short data type can store
		short shortMax = 32767;
// this is the minimum the short data type can store
		short shortMin = -32768;
// long is similar to integer, but it stores larger values
// always use the 'L' for a long number. Otherwise java will
// think that it is an integer and throw an out of range error
// this is the maximum the long data type can store
		long longMax = 9223372036854775807L;
// this is the minimum the long data type can store
		long longMin = -9223372036854775808L;
// byte is similar to integer, but it stores byte values
// this is the maximum the byte data type can store
		byte byteMax = 127;
// this is the minimum the byte data type can store
		byte byteMin = -128;
	}

}
